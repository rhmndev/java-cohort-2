package sesi_pertama;

public class Looping {

    public static void main(String[] args) {

        // FOR
        // angka 10
        // output 1 ....10
        for (int i = 0; i < 10; i++) { // Di dalam Looping terdapat KONDISI
            System.out.println("Loop: " + i);
        }










    }
}
